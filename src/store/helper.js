const drawDot = (ctx, row, col, cell, color = 'red') => {
  const x = row*cell;
  const y = col*cell;
  const radius = 2;

  ctx.beginPath();            // Start a new path
  ctx.arc(x, y, radius, 0, Math.PI * 2); // Draw a full circle
  ctx.fillStyle = color;      // Set the dot color
  ctx.fill();                 // Fill the circle
  ctx.closePath();            // Good practice to close the path
};

